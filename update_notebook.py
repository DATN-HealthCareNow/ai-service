import json

with open("code.ipynb", "r", encoding="utf-8") as f:
    notebook = json.load(f)

# Tìm ô chứa mã nguồn huấn luyện mô hình
for cell in notebook['cells']:
    if cell['cell_type'] == 'code' and any('joblib.dump' in line for line in cell['source']):
        # Cập nhật source của cell này
        new_source = [
            "import pandas as pd\n",
            "import numpy as np\n",
            "\n",
            "from sklearn.model_selection import train_test_split, KFold, cross_val_score, RandomizedSearchCV\n",
            "from sklearn.preprocessing import StandardScaler, OneHotEncoder\n",
            "from sklearn.compose import ColumnTransformer\n",
            "from sklearn.pipeline import Pipeline\n",
            "from sklearn.metrics import mean_absolute_error, mean_squared_error, r2_score\n",
            "from sklearn.ensemble import RandomForestRegressor\n",
            "from sklearn.multioutput import MultiOutputRegressor\n",
            "from xgboost import XGBRegressor\n",
            "import joblib\n",
            "\n",
            "# 1. Đọc dữ liệu\n",
            "df = pd.read_csv(\"wearables_monitoring_data.csv\")\n",
            "\n",
            "# 2. Tính BMI\n",
            "df[\"BMI\"] = df[\"weight\"] / ((df[\"height\"] / 100) ** 2)\n",
            "\n",
            "# 3. Làm sạch\n",
            "df = df[df[\"steps\"] > 10]\n",
            "df = df[df[\"heart_rate\"] < 200]\n",
            "df = df[df[\"calories\"] > 0]\n",
            "df = df.dropna()\n",
            "\n",
            "# 4. Chọn feature\n",
            "feature_cols = [\"steps\", \"BMI\", \"age\", \"gender\", \"distance\"]\n",
            "cat_cols = [\"activity\"]\n",
            "\n",
            "X = df[feature_cols + cat_cols]\n",
            "y = df[[\"heart_rate\", \"calories\"]]\n",
            "\n",
            "# 5. Train/test split\n",
            "X_train, X_test, y_train, y_test = train_test_split(\n",
            "    X, y, test_size=0.2, random_state=42\n",
            ")\n",
            "\n",
            "# 6. Preprocessing pipeline\n",
            "preprocessor = ColumnTransformer(\n",
            "    transformers=[\n",
            "        (\"num\", StandardScaler(), feature_cols),\n",
            "        (\"cat\", OneHotEncoder(handle_unknown=\"ignore\", sparse_output=False), cat_cols),\n",
            "    ],\n",
            "    remainder=\"drop\"\n",
            ")\n",
            "\n",
            "# 7. Tối ưu hóa Random Forest\n",
            "print(\"Bắt đầu tối ưu hóa Random Forest...\")\n",
            "rf_pipeline = Pipeline([\n",
            "    (\"preprocess\", preprocessor),\n",
            "    (\"model\", MultiOutputRegressor(RandomForestRegressor(random_state=42)))\n",
            "])\n",
            "\n",
            "rf_params = {\n",
            "    'model__estimator__n_estimators': [100, 200, 300, 500],\n",
            "    'model__estimator__max_depth': [None, 10, 20, 30],\n",
            "    'model__estimator__min_samples_split': [2, 5, 10],\n",
            "    'model__estimator__min_samples_leaf': [1, 2, 4]\n",
            "}\n",
            "\n",
            "rf_search = RandomizedSearchCV(rf_pipeline, param_distributions=rf_params, \n",
            "                               n_iter=10, cv=3, scoring='neg_mean_absolute_error', \n",
            "                               random_state=42, n_jobs=-1, verbose=1)\n",
            "rf_search.fit(X_train, y_train)\n",
            "best_rf = rf_search.best_estimator_\n",
            "\n",
            "# 8. Tối ưu hóa XGBoost\n",
            "print(\"Bắt đầu tối ưu hóa XGBoost...\")\n",
            "xgb_pipeline = Pipeline([\n",
            "    (\"preprocess\", preprocessor),\n",
            "    (\"model\", MultiOutputRegressor(XGBRegressor(objective=\"reg:squarederror\", random_state=42, verbosity=0)))\n",
            "])\n",
            "\n",
            "xgb_params = {\n",
            "    'model__estimator__n_estimators': [100, 200, 300, 500],\n",
            "    'model__estimator__learning_rate': [0.01, 0.05, 0.1, 0.2],\n",
            "    'model__estimator__max_depth': [3, 4, 6, 8, 10],\n",
            "    'model__estimator__subsample': [0.6, 0.8, 1.0],\n",
            "    'model__estimator__colsample_bytree': [0.6, 0.8, 1.0]\n",
            "}\n",
            "\n",
            "xgb_search = RandomizedSearchCV(xgb_pipeline, param_distributions=xgb_params, \n",
            "                                n_iter=15, cv=3, scoring='neg_mean_absolute_error', \n",
            "                                random_state=42, n_jobs=-1, verbose=1)\n",
            "xgb_search.fit(X_train, y_train)\n",
            "best_xgb = xgb_search.best_estimator_\n",
            "\n",
            "# 9. Hàm đánh giá\n",
            "def evaluate_multi(model, X_eval, y_eval, name=\"Model\"):\n",
            "    y_pred = pd.DataFrame(model.predict(X_eval), columns=y_eval.columns)\n",
            "    print(f\"\\n===== {name} Evaluation =====\")\n",
            "    for col in y_eval.columns:\n",
            "        mae = mean_absolute_error(y_eval[col], y_pred[col])\n",
            "        rmse = np.sqrt(mean_squared_error(y_eval[col], y_pred[col]))\n",
            "        r2 = r2_score(y_eval[col], y_pred[col])\n",
            "        print(f\"{col}: MAE={mae:.3f}, RMSE={rmse:.3f}, R2={r2:.3f}\")\n",
            "\n",
            "evaluate_multi(best_rf, X_test, y_test, \"Optimized Random Forest\")\n",
            "evaluate_multi(best_xgb, X_test, y_test, \"Optimized XGBoost\")\n",
            "\n",
            "# 10. Hàm dự đoán sample\n",
            "def predict_heart_calories(steps, age, weight, height, gender, distance, activity):\n",
            "    BMI = weight / ((height / 100) ** 2)\n",
            "    sample = pd.DataFrame([{\n",
            "        \"steps\": steps,\n",
            "        \"BMI\": BMI,\n",
            "        \"age\": age,\n",
            "        \"gender\": gender,\n",
            "        \"distance\": distance,\n",
            "        \"activity\": activity\n",
            "    }])\n",
            "    rf_pred = best_rf.predict(sample)[0]\n",
            "    xgb_pred = best_xgb.predict(sample)[0]\n",
            "    print(\"RF:\", dict(zip(y.columns, rf_pred)))\n",
            "    print(\"XGB:\", dict(zip(y.columns, xgb_pred)))\n",
            "\n",
            "# 11. Lưu pipeline\n",
            "joblib.dump(best_rf, \"ai-service/app/models/rf_pipeline.pkl\")\n",
            "joblib.dump(best_xgb, \"ai-service/app/models/xgb_pipeline.pkl\")\n"
        ]
        cell['source'] = new_source
        break

with open("code.ipynb", "w", encoding="utf-8") as f:
    json.dump(notebook, f, indent=1)
    
